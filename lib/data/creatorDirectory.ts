export type Platform = 'tiktok' | 'instagram' | 'youtube' | 'pinterest';

export type CreatorFlag = 'none' | 'competitor' | 'avoid';

export type Creator = {
  id: string;
  displayName: string;
  niche: string;
  tags: string[];
  flag: CreatorFlag;
  whyFollow: string;
  whatYouLearn: string;
  partnershipPotential: number;
  partnershipNote: string;
  platforms: Array<{
    platform: Platform;
    handle?: string;
    url?: string;
    audienceCountText?: string;
  }>;
};

export function buildPlatformUrl(platform: Platform, handle?: string): string | undefined {
  if (!handle) return undefined;

  switch (platform) {
    case 'tiktok':
      return `https://www.tiktok.com/@${handle}`;
    case 'instagram':
      return `https://www.instagram.com/${handle}/`;
    case 'pinterest':
      return `https://www.pinterest.com/${handle}/`;
    case 'youtube':
      return `https://www.youtube.com/@${handle}`;
    default: {
      const exhaustive: never = platform;
      return exhaustive;
    }
  }
}

